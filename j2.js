$(document).ready(function(){
    var pic = 0;  
    $(".img1").click(function(){
    if(pic == 0) {
        $(".img1").attr("src","ninja0.png");
        pic = 1;
    }
    else if(pic == 1) {
        $(".img1").attr("src","cat0.png");
        pic = 0;
    }
    });
});
